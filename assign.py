a = 8
b = 4
print('Assign Values:\t', 'a=' , a, '\tb =', b)

a += b
print('Add and Assign:\t\t', 'a=', a, '(8+4)')

a -= b
print('Subtract and Assign:\t\t', 'a=', a, '(12-4)')

a *= b
print('Multiply and Assign:\t\t', 'a=', a, '(8*4)')

a /= b
print('Divide and Assign:\t\t', 'a=', a, '(32/4)')

a %= b
print('Modulo and Assign:\t\t', 'a=', a, '(8%4)')
